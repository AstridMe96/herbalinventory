    private void saveDataBaruButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveDataBaruButtonActionPerformed
        
        try {
            User orangBaru = new User();
            orangBaru.setUsername(usernameDataBarTextFIELD.getText());
            orangBaru.setPassword(passwordDataBarTextField.getText());
            
            String tanggalBaru = tglBuatDataBarTextField.getText();
            
            
            Date date1 =  new SimpleDateFormat("MMM d, yyyy").parse(tanggalBaru);
            tanggalBaru = new SimpleDateFormat("yyyy-MM-dd").format(date1);
            
            
            orangBaru.setTglBuat(tanggalBaru);
            orangBaru.setUsertype(userTypeComboBox.getSelectedItem().toString());
            orangBaru.setAksesDataBarang(dataBarangCheckBox.isSelected());
            orangBaru.setAksesDataPelanggan(dataPelangganCheckBox.isSelected());
            orangBaru.setAksesDataPenjualan(dataPenjualanCheckBox.isSelected());
            
            DataBaseOperation engine = new DataBaseOperation();
            
            if (saveDataBaruButton.getText().equalsIgnoreCase("save")) {
                
                
                engine.addDataUser(orangBaru);
                
                System.out.println("Data Berhasil Tersimpan");
            } else {
                
                int noID = Integer.parseInt(labelID.getText());
                
                engine.updateDataUser(orangBaru,noID);
                
                System.out.println("Data Berhasil Terupdate");
            }
            
            windowMainFrame.refreshTabel();
            this.dispose();
            
            System.out.println("Data Berhasil Tersimpan");
        } catch (ParseException ex) {
            Logger.getLogger(DataUserFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_saveDataBaruButtonActionPerformed

