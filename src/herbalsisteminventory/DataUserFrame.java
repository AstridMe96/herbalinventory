    private void userTypeComboBoxItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_userTypeComboBoxItemStateChanged
       String jenis = userTypeComboBox.getSelectedItem().toString();
       
        if (jenis.equalsIgnoreCase("admin")){
            dataBarangCheckBox.setSelected(true);
            dataPenjualanCheckBox.setSelected(true);
            dataPelangganCheckBox.setSelected(true);
        } else if (jenis.equalsIgnoreCase("sales")){
            dataBarangCheckBox.setSelected(false);
            dataPenjualanCheckBox.setSelected(true);
            dataPelangganCheckBox.setSelected(true);
        } else if (jenis.equalsIgnoreCase("warehouse")){
            dataBarangCheckBox.setSelected(true);
            dataPenjualanCheckBox.setSelected(false);
            dataPelangganCheckBox.setSelected(false);
        }
    
    }//GEN-LAST:event_userTypeComboBoxItemStateChanged

